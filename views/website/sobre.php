        <h3 >Histórico</h3>
        <p class="text-justify">A empresa Gusmão Empreendimentos, com o objetivo inovador, iniciou suas atividades no ano de 2008 na cidade de Itaituba. Os irmãos Queila Gusmão e Alex Gusmão, com empreendedorismo, deram início ao seu primeiro projeto, o Loteamento Novo Paraíso. A fase inicial foi um sucesso com 450 lotes, todos vendidos.</p>
        <p class="text-justify">
            Visando atender a carência de moradias no município, em novembro de 2009, iniciaram as obras de um novo empreendimento, um projeto residencial, a venda de imóveis através do programa minha casa minha vida.
        </p>
        <p class="text-justify">
            Já com melhor estrutura, em janeiro de 2010, firmaram parceria com a Caixa Econômica Federal para financiamento e venda de imóveis, e, em março de 2010, foram entregues as primeiras unidades construídas, deixando seus clientes felizes pela conquista da casa própria.
        </p>
        <p class="text-justify">
            Em 2011 a empresa Gusmão Empreendimentos passou a atuar somente no planejamento e execução das obras residenciais, pois as vendas ficaram na responsabilidade de uma nova empresa que nasceu da parceria entre os irmãos Queila Gusmão e Gutemberg Gusmão: a Kananda Negócios Imobiliários, que, em janeiro de 2011 fixou-se como Credenciada Caixa, onde passou a ofertar os produtos e serviços do Caixa Aqui Negocial.
        </p>
        <p class="text-justify">
            No ano de 2012, a Kananda inicia o projeto de vendas da segunda fase do Loteamento Novo Paraíso, o lançamento dessa nova etapa, se deu pela grande satisfação e confiança dos clientes que compraram lotes na etapa anterior, além da eminente demanda de clientes interessados em adquirir lotes.
        </p>
        <p class="text-justify">
            Em 2013, a Kananda firmou um novo e importante contrato, a legalização do Loteamento Campo Belo. Contrato audacioso, pois seria a primeira legalização de Loteamentos a nível estadual, já que este loteamento apresentava em seu projeto mais de cinco mil lotes.
        </p>
        <p class="text-justify">
            A legalização do Loteamento Campo Belo foi concluída com êxito em junho de 2014, e propiciou o lançamento das vendas em outubro daquele ano, onde, a Kananda participou coordenando uma das três equipes de venda, e por seu sucesso nas mesmas, hoje tem a exclusividade de vendas deste empreendimento.
        </p>
        <p class="text-justify">
            No Ano de 2015 a kananda realizou mais uma conquista para melhor atender seus clientes e parceiros: adquiriu sua sede Própria. 
        </p>
        <p class="text-justify">
            Nesse ano de 2016 implementou um novo projeto, pois está disponibilizando um novo canal de acesso na web, o site www.kananda.imb.br.
        </p>
        <p class="text-justify">
            Desta forma, a Kananda Negócios Imobiliários vem cada vez mais se solidificando no mercado imobiliário local. Isso se deve pela implementação de suas ações empresariais inovadoras, sua busca constante de parcerias, pela excelência na realização dos serviços e projetos em que participa.
        </p>

